def get_category_from_rules(text, user_id=None):
    """Ищет правило нормализации: сначала исправления пользователя, потом AI-правила."""
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()
    # user_correction имеет высший приоритет
    c.execute("SELECT category FROM normalization_rules WHERE original_text=? AND source='user_correction' ORDER BY created_at DESC LIMIT 1", (text,))
    row = c.fetchone()
    if row:
        conn.close()
        return row[0]
    # затем AI
    c.execute("SELECT category FROM normalization_rules WHERE original_text=? AND source='ai' ORDER BY created_at DESC LIMIT 1", (text,))
    row = c.fetchone()
    conn.close()
    if row:
        return row[0]
    return None

def normalize_category(text, user_id=None):
    """Возвращает нормализованную категорию с учётом правил и словаря."""
    # Сначала правила из таблицы
    rule_cat = get_category_from_rules(text, user_id)
    if rule_cat:
        return rule_cat
    # Затем ключевые слова
    text_lower = text.lower()
    for category, keywords in CATEGORY_KEYWORDS.items():
        if any(keyword in text_lower for keyword in keywords):
            return category
    # Если ничего не подошло
    return "Другое"